package hocng.entity;

public class CATEGORIES {
	String id;
	String name;

	public CATEGORIES() {
		super();
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
